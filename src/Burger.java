public class Burger {
    String bread;
    String meat;
    String greens;
    String mayonnaise;
    String cheese;

    public Burger(String bread, String meat, String greens, String mayonnaise, String cheese) {
        this.bread = bread;
        this.meat = meat;
        this.greens = greens;
        this.mayonnaise = mayonnaise;
        this.cheese = cheese;
    }

    public Burger(String bread, String meat, String greens, String cheese) {
        this.bread = bread;
        this.meat = meat;
        this.greens = greens;
        this.cheese = cheese;
    }



}


